void main() {
  // Example of int data type
  int age = 25;
  print('Age: $age'); // Output: Age: 25

  // Example of double data type
  double height = 5.9;
  print('Height: $height'); // Output: Height: 5.9

  // Example of String data type
  String name = 'John';
  print('Name: $name'); // Output: Name: John

  // Example of List data type (list of integers)
  List<int> numbers = [1, 2, 3, 4, 5];
  print('Numbers: $numbers'); // Output: Numbers: [1, 2, 3, 4, 5]

  // Example of Map data type (map of strings to integers)
  Map<String, int> scores = {'Math': 90, 'Science': 85, 'English': 95};
  print(
      'Scores: $scores'); // Output: Scores: {Math: 90, Science: 85, English: 95}
}
